struct Rule {
    start: u32,                          // codepoint for 'A' in the glyph
    except: Option<HashMap<char, char>>, // any exception in codepoint sequence
}

impl Rule {
    const fn new(start: u32) -> Self {
        Self {
            start,
            except: None,
        }
    }

    fn with_except(start: u32, map: impl Into<HashMap<char, char>>) -> Self {
        Self {
            start,
            except: Some(map.into()),
        }
    }
}

impl From<Glyph> for Rule {
    fn from(value: Glyph) -> Self {
        match value {
            Glyph::Bold => Self::new(0x1d400),
            Glyph::Italic => Self::with_except(0x1d434, [('h', '\u{210e}')]),
            Glyph::BoldItalic => Self::new(0x1d468),
            Glyph::Script => Self::with_except(
                0x1d49c,
                [
                    ('B', '\u{212c}'),
                    ('E', '\u{2130}'),
                    ('F', '\u{2131}'),
                    ('H', '\u{210b}'),
                    ('I', '\u{2110}'),
                    ('L', '\u{2112}'),
                    ('M', '\u{2133}'),
                    ('R', '\u{211b}'),
                    ('e', '\u{212f}'),
                    ('g', '\u{210a}'),
                    ('o', '\u{2134}'),
                ],
            ),
            Glyph::BoldScript => Self::new(0x1d4d0),
            Glyph::Fraktur => Self::with_except(
                0x1d504,
                [
                    ('C', '\u{212d}'),
                    ('H', '\u{210c}'),
                    ('I', '\u{2111}'),
                    ('R', '\u{211c}'),
                    ('Z', '\u{2128}'),
                ],
            ),
            Glyph::DoubleStruck => Self::with_except(
                0x1d538,
                [
                    ('C', '\u{2102}'),
                    ('H', '\u{210d}'),
                    ('N', '\u{2115}'),
                    ('P', '\u{2119}'),
                    ('Q', '\u{211a}'),
                    ('R', '\u{211d}'),
                    ('Z', '\u{2124}'),
                ],
            ),
            Glyph::BoldFraktur => Self::new(0x1d56c),
            Glyph::SansSerif => Self::new(0x1d5a0),
            Glyph::SansSerifBold => Self::new(0x1d5d4),
            Glyph::SansSerifItalic => Self::new(0x1d608),
            Glyph::SansSerifBoldItalic => Self::new(0x1d63c),
            Glyph::Monospace => Self::new(0x1d670),
        }
    }
}

// convert an alphabetic char to relative from 'A'
const fn try_diff(c: char) -> Option<u32> {
    match c {
        'A'..='Z' => Some(c as u32 - 'A' as u32),
        'a'..='z' => Some(c as u32 - 'a' as u32 + 26),
        _ => None,
    }
}

/// Convert string into fancy glyphs
///
/// Convert `input` into the specified glyph.  
/// Note that only ASCII alphabets (e.g. **A-Z** and **a-z**) are converted and otherwise left untouched.  
///
/// ```rust
/// use unicode_fancy_glyph::{Glyph, convert};
///
/// let input = "hello ä¸–ç•Œ";
/// let output = convert(Glyph::Fraktur, input);
/// assert_eq!(output, "ğ”¥ğ”¢ğ”©ğ”©ğ”¬ ä¸–ç•Œ");
/// ```
pub fn convert<T: AsRef<str>>(glyph: Glyph, input: T) -> String {
    let Rule { start, except } = Rule::from(glyph);
    let input = input.as_ref();

    // core of conversion
    let conv = move |c| {
        try_diff(c).map_or(c, |diff| {
            // SAFETY: the author throughly-checked Unicode glyph table
            unsafe { std::char::from_u32_unchecked(start + diff) }
        })
    };

    let chars = input.chars();
    if let Some(map) = except {
        // beware, exception exists
        chars
            .map(|ch| map.get(&ch).copied().unwrap_or_else(|| conv(ch)))
            .collect()
    } else {
        // there's no exception, go easy
        chars.map(conv).collect()
    }
}

